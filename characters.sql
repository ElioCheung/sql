-- use % before characters
SELECT *
FROM employees
WHERE last_name LIKE "%r";
-- +-------------+------------+-----------+------------+------+------------+
-- | employee_id | first_name | last_name | hourly_pay | job  | hire_date  |
-- +-------------+------------+-----------+------------+------+------------+
-- |           4 | Patrick    | Star      |      12.50 | cook | 2023-02-01 |
-- +-------------+------------+-----------+------------+------+------------+
