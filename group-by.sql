SELECT SUM(amount),
  order_date
FROM transactions
GROUP BY order_date;
-- +-------------+---------------------+
-- | SUM(amount) | order_date          |
-- +-------------+---------------------+
-- |        7.88 | 2023-01-01 00:00:00 |
-- |       11.10 | 2023-01-02 00:00:00 |
-- |        7.97 | 2023-01-03 00:00:00 |
-- +-------------+---------------------+
