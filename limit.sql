-- LIMIT
-- 常用于分页
SELECT *
FROM employees
LIMIT 2;
-- +-------------+------------+-----------+------------+---------+------------+
-- | employee_id | first_name | last_name | hourly_pay | job     | hire_date  |
-- +-------------+------------+-----------+------------+---------+------------+
-- |           1 | Eugene     | Krabs     |      25.50 | manager | 2023-01-02 |
-- |           2 | Squidward  | Tentacles |      15.00 | cashier | 2023-01-03 |
-- +-------------+------------+-----------+------------+---------+------------+
--
-- use LIMIT with ORDER BY
SELECT *
FROM customers
ORDER BY last_name ASC
LIMIT 2;
-- +----+------------+-----------+---------------------+
-- | id | first_name | last_name | created_at          |
-- +----+------------+-----------+---------------------+
-- |  3 | Bubble     | Bass      | 2023-10-01 14:02:36 |
-- |  1 | Fred       | Fish      | 2023-10-01 14:02:36 |
-- +----+------------+-----------+---------------------+
--
-- use OFFSET
-- 偏移量：2；查询数量：1
SELECT *
FROM customers
LIMIT 2, 1;
-- +----+------------+-----------+---------------------+
-- | id | first_name | last_name | created_at          |
-- +----+------------+-----------+---------------------+
-- |  3 | Bubble     | Bass      | 2023-10-01 14:02:36 |
-- +----+------------+-----------+---------------------+